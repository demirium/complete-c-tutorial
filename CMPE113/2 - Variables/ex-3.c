#include <stdio.h>

int main(void) {
    int score = 50; // We define the score variable 50 at first
    printf("Initial score: %d\n", score); // Output will be 50

    score = score + 10; // We update the score variable by adding 10
    printf("Updated score: %d\n", score); // Output will be 60

    return 0;
}